import 'package:flutter/material.dart';
import 'game.dart';

void main() {
  runApp(const MainApp());
}
class Tile extends StatelessWidget {
const Tile(this.letter, this.hitType, {super.key});
final String letter; // La lettre à afficher (ex. 'A')
final HitType hitType; // Le résultat de la devinette
@override
Widget build(BuildContext context) {
return Container(
width: 60,
height: 60,
decoration: BoxDecoration(
border: Border.all(color: Colors.grey.shade300),
color: switch (hitType) {
HitType.hit => Colors.green,
HitType.partial => Colors.yellow,
HitType.miss => Colors.grey,
_ => Colors.white,
},
),
// Propriété child du Container
child: Center(
child: Text(
letter.toUpperCase(), // On affiche toujours en majuscule
style: Theme.of(context).textTheme.titleLarge,
),
),
);
}}
class MainApp extends StatelessWidget {
const MainApp({super.key});
@override
Widget build(BuildContext context) {
return MaterialApp(
home: Scaffold(
appBar: AppBar( // Ajout de l'AppBar
title: Align(
alignment: Alignment.centerLeft, // Titre aligné à gauche
child: Text('Birdle'),
),
),
body: Center(child: GamePage()), // Inchangé pour l'instant
),
);
}
}
class GamePage extends StatelessWidget {
GamePage({super.key});
final Game _game = Game();

@override
Widget build(BuildContext context) {
  _game.guess('aback',);
  _game.guess('abate',);
  _game.guess('abhor',);
  _game.guess('abide',);
  _game.guess('abode',);
  return Center(
    child: Padding(
      padding: const EdgeInsets.all(8.0),
      child: Column(
        spacing: 5.0,
        crossAxisAlignment: CrossAxisAlignment.center,
        children: [
          for (var guess in _game.guesses)
            Row(
              spacing: 5.0,
              children: [
                for (var letter in guess)
                  Tile(letter.char, letter.type),
              ],
            ),
        ],
      ),
    ),
  );
}

}

